public class FoodKind {
   private int fdkNo; // 음식종류번호
   private String fdkKinds; // 음식종류
   
   public int getFdkNo() {
      return fdkNo;
   }
   public void setFdkNo(int fdkNo) {
      this.fdkNo = fdkNo;
   }
   public String getFdkKinds() {
      return fdkKinds;
   }
   public void setFdkKinds(String fdkKinds) {
      this.fdkKinds = fdkKinds;
   }
}

