package kr.co.jinkyu.pcroom.vo;

public class Fare {
	private int fareNo; // 요금번호
	private int fareTime; // 요금시간
	private int farePrice; // 요금시간
	public int getFareNo() {
		return fareNo;
	}
	public void setFareNo(int fareNo) {
		this.fareNo = fareNo;
	}
	public int getFareTime() {
		return fareTime;
	}
	public void setFareTime(int fareTime) {
		this.fareTime = fareTime;
	}
	public int getFarePrice() {
		return farePrice;
	}
	public void setFarePrice(int farePrice) {
		this.farePrice = farePrice;
	}
	
}
